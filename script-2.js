// 変数の復習
let number = 0; // 変数はlet,定数はconst
number += 1; // numberに1を足す
console.log(number); // 出力

// while文
let number = 1;
while(number<=100){ //変数の値が100以下の時に繰り返す
	console.log(number); // 繰り返す処理の記載
	number += 1; // 更新する値を必ず記載する
}

// for分
for(let number=1; number<=100; number++){
	// 変数の定義　// 条件式　// 変数の更新
	console.log(number);
}